 <h1 style="font-size:30px;"> Reporting bugs </h1>
<h4>
 <em style="color:green;font-size:30px;">1. </em> If you find a bug in Windows, you can basically wait and pray that Microsoft 
 will fix it fast (and if it compromises your system's security, you would have to pray twice as hard). You might think that reporting
 that bug to Microsoft (so that they can fix it more quickly) must be easy.
<div class="hr1" style="margin-bottom:8px;"></div>
<em style="color:green;font-size:30px;">2. </em> Well, think again. Here is an interesting article about this. What if Microsoft
 doesn't even notice the bug? Well then, let's hope the next version of Windows will fix it (but you'll need to pay another few
  hundred bucks).
<div class="hr1" style="margin-bottom:8px;"></div>
<em style="color:green;font-size:30px;">3. </em> Nearly all open source software (including Linux distributions) have a bug
 tracking system. You can not only file bug reports (and you're encouraged to do so!) explaining what the problem is, but you can
 see what happens next : everything is open and clear for everyone. 
<div class="hr1" style="margin-bottom:8px;"></div>
<em style="color:green;font-size:30px;">4. </em> Developers will answer, they also might ask a little extra information to help
 them fix the bug. You will know when the bug has been fixed, and you will know how to get the new version (still for free, 
 needless to say). 
<div class="hr1" style="margin-bottom:8px;"></div>
<em style="color:green;font-size:30px;">5. </em> So here you have people taking care of your problems, keeping you  informed
 about it, and all that for free! If the problem is solved on your system, it will be on everyone else's : it's in everyone's interest to
 work together to make software better. This is how open source works.
