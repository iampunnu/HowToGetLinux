<h4>
<em style="color:green;font-size:25px;">1. </em>Linux and "Open Source" software are "free". This means their license 
  is a "free license", and the most common is
    the GPL (General Public License). This license states that anyone is allowed to copy the software, see the source
    code (the "recipe"), modify it, and redistribute it as long as it remains licensed with the GPL.<br><br>
  <em style="color:green;font-size:25px;">2. </em> So what do you care about freedom? Imagine that Microsoft disappears
   tomorrow (okay, that's not very likely,
    but what about in 5 years, 10 years?). Or imagine it suddenly triples the price for a Windows or Office license.<br><br>
  <em style="color:green;font-size:25px;">3. </em> If you're tied to Windows, there's nothing you can do. You 
  (or your business) relies on this one company, on its software,
    and you can't possibly make things work without it (what good is a computer without an operating system?).<br><br>
  <em style="color:green;font-size:25px;">4. </em> Isn't that a serious problem? You're depending on one single company
   and trusting it wholeheartedly to let 
   something so important nowadays as your computers work the way they should.<br><br>
  <em style="color:green;font-size:25px;">5. </em> If Microsoft decides to charge $1000 for the next version of Windows, 
  there's nothing you can do about it 
   (except switch to Linux, of course). If Windows has a bug that bothers you very much and Microsoft won't fix it,
   there's nothing you can do (and submitting bugs to Microsoft isn't that easy, see the "Report bugs" section).<br><br>
 <em style="color:green;font-size:25px;">6. </em> With Open Source, if a particular project or support company dies, all
  the code remains open to the community and 
  people can keep improving it. If this project is especially useful to you, you can even do this yourself.<br><br>
<em style="color:green;font-size:25px;">7. </em> If a particular bug annoys you, you can submit it, talk with the developers,
  but even better, you can fix it yourself 
 (or hire someone to do so), and send the changes back to the upstream developers so that everyone gets the 
  improvement as well. You're free to do (nearly) whatever you want with the software.<br><br> 
   

