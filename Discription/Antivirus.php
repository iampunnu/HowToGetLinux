<h4>
 If your computer shuts itself down without asking you, if emails get sent to all your contacts 
 without your knowing it, then your computer probably has a virus. 
 The main reason for this is because it runs Windows.<br>
 Linux hardly has any viruses. And that's not like "Oh well, not very often, you know". That's like "If you've ever heard 
 of a real Linux virus". <br>Of course, a Linux virus is not impossible to get. However, Linux makes it very
  hard for this to happen, for several reasons.<br><br>
        
        1. Most people use Microsoft Windows, and pirates want to do as much damage (or control) as possible: therefore, 
        they target Windows. But that's not the only reason; the Apache web server, apache which is open source software,
        has the biggest market share (against Microsoft's IIS server), but it still suffers from much fewer attacks/flaws than
        the Microsoft one.<br><br>


        2. Linux uses smart authorization management. In Windows you (and any program you install) usually have the right to do 
        pretty much anything to the system. If you feel like punishing your PC because it just let your precious work disappear,
        you can go inside the system folder and delete whatever you want: Windows won't complain. Of course, the next time you
        reboot, trouble begins. But imagine that if you can delete this system stuff, other programs can, too, or just mess it up.
        Linux doesn't allow that. Every time you request to do something that has to do with the system, an administrator password
        is required (and if you're not an administrator on this system, you simply can't do it). Viruses can't just go around and delete
        or modify what they want in the system; they don't have the authorization for that.<br><br>


       3. More eyes make fewer security flaws. Linux is Open source software, which means that any programmer in the world can 
       have a look at the code (the "recipe" of any program), and help out, or just tell other developers "Hey, what if blah blah,
       isn't this a security flaw?".             
  
       
