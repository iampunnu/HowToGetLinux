 <h1 style="font-size:30px;"> Forget about drivers </h1>
<h4>
  1.New pieces of hardware, even the simplest kind, usually come with a CD. On the CD, a very small piece of software called a "driver".
   <br>If you read the instructions manual, you'll know that the hardware won't work on a Windows computer until you 
   install the driver.<br><br>
   2. If you're like most people and do not read the manual, then you'll probably figure it out yourself when you see your new 
   high-tech gizmo doesn't work out of the box. <br>Insert CD, click on installation wizard, wait, eject CD, reboot computer.
  If you bought the hardware a while ago and are re-using it on another computer, you'll probably want to forget about 
  the CD and fetch the latest version of the driver from the manufacturer's website.<br> Which can take quite a bit of time,
  given how, huh, let's say strangely organized some manufacturers' web sites are.<br><br>
 3. Okay, now that's only one piece of hardware. Now imagine you want to install Windows on a whole new, untouched,
  computer. For each little piece of hardware you'll have to find the latest driver (or use a CD), install it, and reboot from 
  time to time.<br> Video card, sound card, keyboard, mouse, motherboard chipset, etc. (better do the video card driver first or
  you're stuck with your high-end screen showing a very low resolution mode). And that comes after an already rather long
  installation of Windows itself.<br><br>
   4. Linux doesn't need separate drivers. All the drivers are already included in the Linux kernel, the core of the system,
   and that comes with every single Linux installation. This means:<br><br>
   i) A very fast and standalone installation process. Once you're done, you have everything you need to start working 
   (including the software you'll be using, see "When the system has installed..." item on this website).<br> 
   ii) Out-of-the-box ready peripherals.<br>
   iii) Less harm for the planet because all these CDs don't need to come with hardware any more
     (well, at least once Windows don't need them either...).<br>
 





