<h4>
 1. How can Linux be different from Windows when it comes to environment, you might ask? After all, they're both just
  pieces of software with little impact on pollution or climate change. Well, choosing Linux can actually have an influence on 
  the environment:
  <div class="hr1" style="margin-bottom:8px;"></div>
  <em style="color:red;font-size:30px;">i). </em> Windows and Mac OS are sold in boxes. This means that massive amounts of paper and plastic need to be 
  manufactured before the boxes get to your nearby store's shelves (and be disposed of after you buy them).
   Linux is freely downloadable from the Internet; no amount of plastic or paper is involved.
  <div class="hr1" style="margin-bottom:8px;"></div>
 <em style="color:red;font-size:30px;">ii). </em> Proprietary applications for Windows or Mac OS are also, most of the time, sold in local stores, in boxes, 
 whereas you can download the vast majority of software for Linux from the Internet, for free (again, a whole lot of
  saved paper and plastic!).   
  <div class="hr1" style="margin-bottom:8px;"></div>
 <em style="color:red;font-size:30px;">iii). </em> As the hardware requirements for Windows or Mac OS get higher and higher, a lot of computers are made obsolete,
  and would need to be disposed of... but since Linux runs pretty well even on very old machines, they can be recycled 
  for various purposes (storage, internet access, multimedia box, etc.) instead of being thrown out! 
  <div class="hr1" style="margin-bottom:8px;"></div>
 <em style="color:red;font-size:30px;">iv). </em> Millions of CDs are pressed to hold Windows or Mac OS boxes and are sold
   to customers. Linux also needs to be burnt on a CD before installation (in most cases at least -- installation from the network or
    from a hard disk is also quite common). However, most people choose to burn it on a rewritable CD ("CD-RW"), which
   can be reused for other purposes after the installation is over (unlike proprietary operating systems, you don't need to keep the CD
    around after you've installed the software, you can always download it again later).

